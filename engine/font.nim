proc drawBitmapText*(renderer: SDL_Renderer, x, y: float32, text: string,
                     color: array[4, uint8], scale: float32 = 2f,
                     shadow: bool = true) =
  var cursorX = x
  var cursorY = y
  for ch in text:
    if ch == '\n':
      cursorX = x
      cursorY += (7f * scale) + 2f
      continue
    if ch == ' ':
      cursorX += 4f * scale
      continue
    let bitmap = glyphBitmap(ch)
    if shadow:
      for row in 0 ..< bitmap.len:
        let rowStr = bitmap[row]
        for col in 0 ..< rowStr.len:
          if rowStr[col] == '1':
            var rect = SDL_FRect(
              x: cursorX + float32(col) * scale + 1f,
              y: cursorY + float32(row) * scale + 1f,
              w: scale,
              h: scale
            )
            discard SDL_SetRenderDrawColor(renderer, 0'u8, 0'u8, 0'u8, 160'u8)
            discard SDL_RenderFillRect(renderer, addr rect)
    for row in 0 ..< bitmap.len:
      let rowStr = bitmap[row]
      for col in 0 ..< rowStr.len:
        if rowStr[col] == '1':
          var rect = SDL_FRect(
            x: cursorX + float32(col) * scale,
            y: cursorY + float32(row) * scale,
            w: scale,
            h: scale
          )
          discard SDL_SetRenderDrawColor(renderer, color[0], color[1], color[2], color[3])
          discard SDL_RenderFillRect(renderer, addr rect)
    cursorX += 6f * scale
